#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const kbDir = path.join(root, 'docs', 'mingli');
const sourcesPath = path.join(kbDir, 'sources.json');
const claimsPath = path.join(kbDir, 'claims.json');
const generatedMdPath = path.join(kbDir, 'SOURCES.generated.md');
const generatedIndexPath = path.join(kbDir, 'source-index.json');

function readJson(file) {
  return JSON.parse(fs.readFileSync(file, 'utf8'));
}

function die(errors) {
  for (const err of errors) console.error(`mingli-kb: ${err}`);
  process.exit(1);
}

const sourceDoc = readJson(sourcesPath);
const claimDoc = readJson(claimsPath);
const errors = [];

if (!Array.isArray(sourceDoc.sources)) errors.push('sources must be an array');
if (!Array.isArray(claimDoc.claims)) errors.push('claims must be an array');
if (errors.length) die(errors);

const requiredSourceFields = ['id', 'title', 'domain', 'tier', 'sourceType', 'currentUsage', 'ingestStatus', 'ingestPriority'];
const requiredClaimFields = ['id', 'domain', 'claim', 'sourceIds', 'confidence', 'status'];
const sourceIds = new Set();

for (const source of sourceDoc.sources) {
  for (const field of requiredSourceFields) {
    if (source[field] == null || source[field] === '') errors.push(`${source.id ?? '(missing id)'} missing ${field}`);
  }
  if (source.id && sourceIds.has(source.id)) errors.push(`duplicate source id: ${source.id}`);
  if (source.id) sourceIds.add(source.id);
  if (!Array.isArray(source.domain) || source.domain.length === 0) errors.push(`${source.id} domain must be a non-empty array`);
  if (!['A', 'B', 'C', 'D'].includes(source.tier)) errors.push(`${source.id} tier must be A/B/C/D`);
}

const claimIds = new Set();
for (const claim of claimDoc.claims) {
  for (const field of requiredClaimFields) {
    if (claim[field] == null || claim[field] === '') errors.push(`${claim.id ?? '(missing id)'} missing ${field}`);
  }
  if (claim.id && claimIds.has(claim.id)) errors.push(`duplicate claim id: ${claim.id}`);
  if (claim.id) claimIds.add(claim.id);
  if (!Array.isArray(claim.sourceIds) || claim.sourceIds.length === 0) errors.push(`${claim.id} sourceIds must be a non-empty array`);
  for (const sourceId of claim.sourceIds ?? []) {
    if (!sourceIds.has(sourceId)) errors.push(`${claim.id} references missing source: ${sourceId}`);
  }
}

if (errors.length) die(errors);

const sourcesByDomain = new Map();
for (const source of sourceDoc.sources) {
  for (const domain of source.domain) {
    if (!sourcesByDomain.has(domain)) sourcesByDomain.set(domain, []);
    sourcesByDomain.get(domain).push(source);
  }
}

for (const list of sourcesByDomain.values()) {
  list.sort((a, b) => `${a.ingestPriority}-${a.tier}-${a.id}`.localeCompare(`${b.ingestPriority}-${b.tier}-${b.id}`));
}

const lines = [];
lines.push('# 有时 · 命理 Sources Index');
lines.push('');
lines.push('> Generated by `npm run kb:mingli`. Do not edit by hand.');
lines.push('');
lines.push(`Updated: ${sourceDoc.updatedAt}`);
lines.push('');
lines.push('## Source tiers');
lines.push('');
for (const [tier, text] of Object.entries(sourceDoc.sourceTiers ?? {})) {
  lines.push(`- **${tier}** — ${text}`);
}
lines.push('');
lines.push('## Domains');
lines.push('');

for (const domain of [...sourcesByDomain.keys()].sort()) {
  lines.push(`### ${domain}`);
  lines.push('');
  for (const source of sourcesByDomain.get(domain)) {
    lines.push(`#### ${source.id}`);
    lines.push('');
    lines.push(`- Title: ${source.title}`);
    if (source.author) lines.push(`- Author: ${source.author}`);
    if (source.period) lines.push(`- Period: ${source.period}`);
    lines.push(`- Tier: ${source.tier}`);
    lines.push(`- Type: ${source.sourceType}`);
    lines.push(`- Status: ${source.ingestStatus}`);
    lines.push(`- Priority: ${source.ingestPriority}`);
    if (source.currentUsage?.length) lines.push(`- Current usage: ${source.currentUsage.join('；')}`);
    if (source.repoRefs?.length) lines.push(`- Repo refs: ${source.repoRefs.map((x) => `\`${x}\``).join(', ')}`);
    if (source.licenseNote) lines.push(`- License note: ${source.licenseNote}`);
    if (source.notes) lines.push(`- Notes: ${source.notes}`);
    lines.push('');
  }
}

lines.push('## Claims');
lines.push('');
for (const claim of [...claimDoc.claims].sort((a, b) => a.id.localeCompare(b.id))) {
  lines.push(`### ${claim.id}`);
  lines.push('');
  lines.push(`- Domain: ${claim.domain}`);
  lines.push(`- Claim: ${claim.claim}`);
  lines.push(`- Sources: ${claim.sourceIds.map((id) => `\`${id}\``).join(', ')}`);
  if (claim.repoRefs?.length) lines.push(`- Repo refs: ${claim.repoRefs.map((x) => `\`${x}\``).join(', ')}`);
  lines.push(`- Confidence: ${claim.confidence}`);
  lines.push(`- Status: ${claim.status}`);
  lines.push('');
}

fs.writeFileSync(generatedMdPath, `${lines.join('\n').trim()}\n`, 'utf8');

const compactIndex = {
  schemaVersion: 1,
  generatedAt: new Date().toISOString(),
  sourceCount: sourceDoc.sources.length,
  claimCount: claimDoc.claims.length,
  domains: Object.fromEntries([...sourcesByDomain.entries()].sort().map(([domain, sources]) => [
    domain,
    sources.map((source) => ({
      id: source.id,
      title: source.title,
      tier: source.tier,
      type: source.sourceType,
      priority: source.ingestPriority,
      status: source.ingestStatus,
      usage: source.currentUsage,
    })),
  ])),
  claims: claimDoc.claims.map((claim) => ({
    id: claim.id,
    domain: claim.domain,
    sourceIds: claim.sourceIds,
    status: claim.status,
    confidence: claim.confidence,
  })).sort((a, b) => a.id.localeCompare(b.id)),
};
fs.writeFileSync(generatedIndexPath, `${JSON.stringify(compactIndex, null, 2)}\n`, 'utf8');

console.log(`mingli-kb: ok — ${sourceDoc.sources.length} sources, ${claimDoc.claims.length} claims`);
console.log(`mingli-kb: wrote ${path.relative(root, generatedMdPath)}`);
console.log(`mingli-kb: wrote ${path.relative(root, generatedIndexPath)}`);
